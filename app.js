// Reveal
const reveals = document.querySelectorAll(".reveal");
const revealObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("show");
      }
    });
  },
  { threshold: 0.12 }
);
reveals.forEach((el) => revealObserver.observe(el));

// Services filter
const serviceSearch = document.getElementById("serviceSearch");
const serviceTag = document.getElementById("serviceTag");
const serviceCards = document.querySelectorAll(".service-card");

function filterServices() {
  const searchValue = (serviceSearch?.value || "").trim().toLowerCase();
  const tagValue = serviceTag?.value || "all";

  serviceCards.forEach((card) => {
    const title = (card.dataset.title || "").toLowerCase();
    const tags = (card.dataset.tags || "").toLowerCase();

    const matchesSearch = title.includes(searchValue) || tags.includes(searchValue);
    const matchesTag = tagValue === "all" || tags.includes(tagValue);

    if (matchesSearch && matchesTag) {
      card.classList.remove("hidden-card");
    } else {
      card.classList.add("hidden-card");
    }
  });
}

serviceSearch?.addEventListener("input", filterServices);
serviceTag?.addEventListener("change", filterServices);

// Preset service
const presetLinks = document.querySelectorAll("[data-preset]");
const contactService = document.querySelector('#contactForm select[name="service"]');
const contactMessage = document.querySelector('#contactForm textarea[name="message"]');

presetLinks.forEach((link) => {
  link.addEventListener("click", () => {
    const preset = link.getAttribute("data-preset");
    if (!preset || !contactService) return;

    const options = [...contactService.options];
    const matchingOption = options.find(
      (opt) => opt.value.trim().toLowerCase() === preset.trim().toLowerCase()
    );

    if (matchingOption) {
      contactService.value = matchingOption.value;
    } else if (contactMessage && !contactMessage.value.trim()) {
      contactMessage.value = `I'm interested in ${preset}. Please share details.`;
    }
  });
});

